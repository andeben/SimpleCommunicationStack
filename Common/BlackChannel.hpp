#ifndef BLACK_CHANNEL_HPP
#define BLACK_CHANNEL_HPP

#include <stdint.h>

///////////////////////////////////////////////////////////////////////////////
///////////						Main Protocol Carrier 			    ///////////
///////////////////////////////////////////////////////////////////////////////
typedef struct {
  uint8_t signo;
  uint8_t payload[100];
} BlackChannelMessage_t;



///////////////////////////////////////////////////////////////////////////////
///////////						SIGNAL DEFINITION     			    ///////////
///////////////////////////////////////////////////////////////////////////////
#define BLACK_CHANNEL_MESSAGE_REQ   			         0x01
#define BLACK_CHANNEL_MESSAGE_REJ 			           0x02

#define BLACK_CHANNEL_PROTOCOL_ID_CSP              0x11

///////////////////////////////////////////////////////////////////////////////
///////////						SIGNAL Payload structures	                    ///////////
///////////////////////////////////////////////////////////////////////////////
typedef struct {
  uint8_t   protocolId;
  uint8_t   payload[100];
} BlackChannelMessagePayload_t;

typedef struct {
  uint8_t   errorCode;
} BlackChannelMessageRej_t;


#endif //BLACK_CHANNEL_HPP
